#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ACCEPTANCE_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
REPO_ROOT="$(cd "${ACCEPTANCE_ROOT}/.." && pwd)"
FENIX_ROOT="${FENIX_PROJECT_ROOT:-${REPO_ROOT}/agents/fenix}"

FENIX_RUNTIME_BASE_URL="${FENIX_RUNTIME_BASE_URL:-http://127.0.0.1:3101}"
FENIX_DOCKER_CONTAINER="${FENIX_DOCKER_CONTAINER:-fenix-capstone}"
FENIX_DOCKER_PROXY_CONTAINER="${FENIX_DOCKER_PROXY_CONTAINER:-fenix-capstone-proxy}"
FENIX_DOCKER_IMAGE="${FENIX_DOCKER_IMAGE:-fenix-capstone-image}"
FENIX_DOCKER_PROXY_PORT="${FENIX_DOCKER_PROXY_PORT:-3310}"
FENIX_DOCKER_WORKSPACE_ROOT="${FENIX_DOCKER_WORKSPACE_ROOT:-${REPO_ROOT}/tmp/fenix}"
FENIX_DOCKER_ENV_FILE="${FENIX_DOCKER_ENV_FILE:-${FENIX_ROOT}/.env}"
FENIX_DOCKER_HOME_ROOT="${FENIX_DOCKER_HOME_ROOT:-/rails/storage/fenix-home}"
FENIX_MACHINE_CREDENTIAL="${FENIX_MACHINE_CREDENTIAL:?FENIX_MACHINE_CREDENTIAL is required}"
FENIX_EXECUTION_MACHINE_CREDENTIAL="${FENIX_EXECUTION_MACHINE_CREDENTIAL:-${FENIX_MACHINE_CREDENTIAL}}"
FENIX_RUNTIME_BOOT_JSON="${FENIX_RUNTIME_BOOT_JSON:-${CAPSTONE_RUNTIME_WORKER_BOOT_PATH:-}}"

export AGENT_PROJECT_ROOT="${FENIX_ROOT}"
export AGENT_RUNTIME_BASE_URL="${FENIX_RUNTIME_BASE_URL}"
export AGENT_DOCKER_CONTAINER="${FENIX_DOCKER_CONTAINER}"
export AGENT_DOCKER_PROXY_CONTAINER="${FENIX_DOCKER_PROXY_CONTAINER}"
export AGENT_DOCKER_IMAGE="${FENIX_DOCKER_IMAGE}"
export AGENT_DOCKER_PROXY_PORT="${FENIX_DOCKER_PROXY_PORT}"
export AGENT_DOCKER_WORKSPACE_ROOT="${FENIX_DOCKER_WORKSPACE_ROOT}"
export AGENT_DOCKER_ENV_FILE="${FENIX_DOCKER_ENV_FILE}"
export AGENT_DOCKER_STORAGE_VOLUME="fenix_capstone_storage"
export AGENT_DOCKER_PROXY_ROUTES_VOLUME="fenix_capstone_proxy_routes"
export AGENT_DOCKER_PROXY_COMMAND="/rails/bin/fenix-dev-proxy"
export AGENT_PUBLIC_BASE_URL_ENV_KEY="FENIX_PUBLIC_BASE_URL"
export AGENT_PROXY_PORT_ENV_KEY="FENIX_DEV_PROXY_PORT"
export AGENT_PROXY_ROUTES_ENV_KEY="FENIX_DEV_PROXY_ROUTES_FILE"
export AGENT_PROXY_ROUTES_PATH="/rails/tmp/dev-proxy/routes.caddy"
export AGENT_MACHINE_CREDENTIAL="${FENIX_MACHINE_CREDENTIAL}"
export AGENT_EXECUTION_MACHINE_CREDENTIAL="${FENIX_EXECUTION_MACHINE_CREDENTIAL}"
export AGENT_RUNTIME_BOOT_JSON="${FENIX_RUNTIME_BOOT_JSON}"
export AGENT_OUTPUT_PREFIX="fenix"
export AGENT_HOME_ROOT_ENV_KEY="FENIX_HOME_ROOT"
export AGENT_HOME_ROOT_PATH="${FENIX_DOCKER_HOME_ROOT}"

exec bash "${SCRIPT_DIR}/activate_agent_docker_runtime.sh"
